package Basics;

//Variables
public class Variable {
     public static void main(String[] args) {
          // case sensitive
          // can be letters,digits,signs or underscore
          // Must begin with a letter, Dollar Sign($) or Underscore(_)
          // Can not use Java Keywords as variable name
          int $age = 23;// Ex-1
          System.out.println($age);
          int _age = 24;// Ex-1
          System.out.println(_age);
          // int 1age = 23;//Ex- Not allowed
          // System.out.println(1age);
          int a = 10;
          double b = 2.5;
          System.out.println(a + " " + b);
     }
}
